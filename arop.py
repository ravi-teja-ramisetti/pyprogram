#Program for aops
a=int(input(("enter the first value:")))
b=int(input(("enter the second value:")))
#print results
print("{} + {}={}".format(a,b,a+b))
print("{} - {}={}".format(a,b,a-b))
print("{} * {}={}".format(a,b,a*b))
print("{} / {}={}".format(a,b,a/b))
print("{} // {}={}".format(a,b,a//b))
print("{} ** {}={}".format(a,b,a**b))
